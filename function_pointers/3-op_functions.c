
/**
  * op_add - adds two numbers.
  * @a: First number.
  * @b: Second number.
  *
  * Return: The addition of the two numbers.
  */

int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - subtracts the first number the second number.
  * @a: First number.
  * @b: Second number.
  *
  * Return: The subtraction of the two numbers.
  */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - multiplies two numbers.
  * @a: First number.
  * @b: Second number.
  *
  * Return: The multiplication of the two numbers.
  */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - divides two numbers.
  * @a: First number.
  * @b: Second number.
  *
  * Return: The division of the first number by the second number.
  */

int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - divides two numbers and returns the remainder of the division.
  * @a: First number.
  * @b: Second number.
  *
  * Return: The remainder of dividing the first number by the second number.
  */

int op_mod(int a, int b)
{
	return (a % b);
}
